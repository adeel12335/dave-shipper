window.CR = window.CR || {};

window.CR.getLang = function () {
  return localStorage.getItem("cr_lang") || (window.CR_CONFIG && window.CR_CONFIG.DEFAULT_LANG) || "fr";
};

window.CR.t = function (key, lang) {
  lang = lang || window.CR.getLang();
  var dict = window.CR_I18N[lang] || window.CR_I18N.fr;
  return dict[key] != null ? dict[key] : key;
};

window.CR.applyLang = function (lang) {
  lang = lang || window.CR.getLang();
  localStorage.setItem("cr_lang", lang);
  document.documentElement.setAttribute("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var v = window.CR.t(el.getAttribute("data-i18n"), lang);
    el.textContent = v;
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(function (el) {
    el.setAttribute("placeholder", window.CR.t(el.getAttribute("data-i18n-ph"), lang));
  });
  document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
    el.innerHTML = window.CR.t(el.getAttribute("data-i18n-html"), lang);
  });

  // update toggle buttons label
  document.querySelectorAll("[data-lang-toggle]").forEach(function (el) {
    el.textContent = window.CR.t("lang.toggle", lang);
  });
};

window.CR.toggleLang = function () {
  var next = window.CR.getLang() === "fr" ? "en" : "fr";
  window.CR.applyLang(next);
};
